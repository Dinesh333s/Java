public class Arthmetic_assignment {

    /*
     += , -= , *= , /= , %=
     */
public static void main(String args[]){
    int a=123;
    System.out.println("a= "+a);
    a+=10;
    System.out.println("a= "+a);
    a-=10;
    System.out.println("a= "+a);
    a*=10;
    System.out.println("a= "+a);
    a/=10;
    System.out.println("a= "+a);
    a%=10;
    System.out.println("a= "+a);
}
}
