public class Bitwise_operator {
    public static void main(String args[])
    {
        //Bitwise and shift operator
        int a = 2 ,b = 3;
        System.out.println("Bitwise & :"+(a&b));
        System.out.println("Bitwise | :"+(a|b));
        System.out.println("Bitwise Xor :"+(a^b));
        System.out.println("Bitwise Not :"+(~a));
    }
}
